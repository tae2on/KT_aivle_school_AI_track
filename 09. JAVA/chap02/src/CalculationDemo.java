import java.math.BigInteger;

public class CalculationDemo {
    public static void main(String[] args) {
        int i;
        long l;
        double d;

        i = 5/2;
        System.out.println("5/2 : " + i);

        d = 5.0/2.0;
        System.out.println("5/2 : " + d);

        l = 10000000L * 10000000L;
        System.out.println(l);
    }
}
